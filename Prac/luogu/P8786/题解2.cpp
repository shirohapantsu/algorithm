#include <iostream>
using namespace std;

const int MOD = 1000000007;

// dp[i][j][k] 表示：遇到 i 次店，j 次花，还剩 k 斗酒的方案数
// 数组稍微开大一点防止 k+1 越界
int dp[105][105][105]; 

int main() {
    int n, m;
    cin >> n >> m;

    // 初始状态：0个店，0朵花，初始有2斗酒，方案数为1
    dp[0][0][2] = 1;

    // i 代表店的数量，j 代表花的数量
    for (int i = 0; i <= n; i++) {
        for (int j = 0; j <= m; j++) {
            
            // 跳过初始状态，因为已经赋值过了
            if (i == 0 && j == 0) continue; 
            
            // k 代表当前剩余的酒量
            // 酒量最多不可能超过 m，否则即使后面全是花也喝不完
            for (int k = 0; k <= m; k++) {
                
                // 情况 1：最后一步是遇到店（前提是店数 i>0，且当前酒量 k 必须是偶数才能逆推）
                if (i > 0 && k % 2 == 0) {
                    dp[i][j][k] = (dp[i][j][k] + dp[i - 1][j][k / 2]) % MOD;
                }
                
                // 情况 2：最后一步是遇到花（前提是花数 j>0）
                // 遇到花之前酒量是 k+1，需要确保 k+1 <= m 防止越界和无效计算
                if (j > 0 && k + 1 <= m) {
                    dp[i][j][k] = (dp[i][j][k] + dp[i][j - 1][k + 1]) % MOD;
                }
                
            }
        }
    }

    // 根据题意，最后一次必然是遇到花，且喝光酒。
    // 这说明在最后一次遇花之前，一定是：遇到了 n 个店，m-1 朵花，并且刚好剩 1 斗酒。
    cout << dp[n][m - 1][1] << endl;

    return 0;
}